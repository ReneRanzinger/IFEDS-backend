package edu.uga.ccrc.view.bean;

public class CreateSampleToSampleDescriptorHelperBean {

	private Long sample_descriptor_id;
	
	private String sample_descriptor_value;
	
	private String unit_of_measurment;

	public Long getSample_descriptor_id() {
		return sample_descriptor_id;
	}

	public void setSample_descriptor_id(Long sample_descriptor_id) {
		this.sample_descriptor_id = sample_descriptor_id;
	}

	public String getSample_descriptor_value() {
		return sample_descriptor_value;
	}

	public void setSample_descriptor_value(String sample_descriptor_value) {
		this.sample_descriptor_value = sample_descriptor_value;
	}

	public String getUnit_of_measurment() {
		return unit_of_measurment;
	}

	public void setUnit_of_measurment(String unit_of_measurment) {
		this.unit_of_measurment = unit_of_measurment;
	}
	

}
